public class Cubesquare_3
{ 
    public static void main (String args[])
    { 
        for (int i = 0; i <= 10; i++)
        { 
            System.out.println ("Sqaures: "); 
            System.out.println (i * i); 
        }

        System.out.println ("***END***"); 

        for (int i = 0; i <= 10; i++)
        { 
            System.out.println ("Cubes: "); 
            System.out.println (i * i * i); 
        } 
        System.out.println ("***END***"); 
    }
}